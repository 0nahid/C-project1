/* C variables */
#include <stdio.h>

int main() {
  // Create variables
  int myNum = 5;                        // Integer (whole number)
  float myFloatNum = 5.9956894854;     // Floating point number
  char myLetter = 'D';                // Character
  
  // Print variables
  printf("%d\n", myNum);
  printf("%.3f\n", myFloatNum);
  printf("%c\n", myLetter);
  return 0;
}